// ─── Helper: compute financials ───────────────────────────────────────────────

function calcLoad(rate: number, miles: number, feePercent: number) {
  const dispatchFeeAmount = Math.round(rate * (feePercent / 100) * 100) / 100;
  const carrierNet = Math.round((rate - dispatchFeeAmount) * 100) / 100;
  const ratePerMile = miles > 0 ? Math.round((rate / miles) * 100) / 100 : 0;
  return { dispatchFeeAmount, carrierNet, ratePerMile };
}

// ─── Loads ────────────────────────────────────────────────────────────────────

function makeLoad(
  id: string, num: string, carrierId: string, override: Partial<SonexLoad> & {
    rate: number; miles: number; feePercent?: number;
  }
): SonexLoad {
  const feePercent = override.feePercent ?? 10;
  const { dispatchFeeAmount, carrierNet, ratePerMile } = calcLoad(override.rate, override.miles, feePercent);
  return {
    id,
    loadNumber: num,
    carrierId,
    brokerName: '',
    brokerContact: '',
    brokerPhone: '',
    pickupFacility: '',
    pickupAddress: '',
    pickupCity: '',
    pickupState: '',
    pickupZip: '',
    pickupDate: '',
    pickupTime: '08:00',
    deliveryFacility: '',
    deliveryAddress: '',
    deliveryCity: '',
    deliveryState: '',
    deliveryZip: '',
    deliveryDate: '',
    deliveryTime: '14:00',
    commodity: '',
    weight: 0,
    status: 'paid',
    dispatchFeePercent: feePercent,
    dispatchFeeAmount,
    carrierNet,
    ratePerMile,
    notes: '',
    createdAt: new Date().toISOString(),
    updatedAt: new Date().toISOString(),
    ...override,
  };
}

